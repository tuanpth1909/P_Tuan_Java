public class CricleComputation {
    public static void main(String[] args) {
        double radius, area, cricumference;
        final double PI = 3.14;
        radius = 1.2;
        area = radius * radius * PI;
        cricumference = 2.0 * radius * PI;
        //Part results
        System.out.println("This radius is:");
        System.out.println(radius);
        System.out.println("This area is:");
        System.out.println(area);
        System.out.println("This cricumference is:");
        System.out.println(cricumference);
    }
}
